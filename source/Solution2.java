public class Solution2 {

  public static void main(String[] args) {
    solution(
        new String[]{"111111111111111111111111110111111111", "001101001101101100101101010101001100",
            "111101111101101111101111010111111111", "100101100101101101101001010101001001",
            "111111111111111111111111111111111111"});

    solution(
        new String[]{"110111101111111111110111", "010101101100101101010100",
            "010111111111101111010111", "010001001001101101010001", "111111001111111111111111"
        });

  }

  static String[][] num = {{"111", "101", "101", "101", "111"},
      {"110", "010", "010", "010", "111"}, {"111", "001", "111", "100", "111"},
      {"111", "001", "111", "001", "111"}, {"101", "101", "111", "001", "001"},
      {"111", "100", "111", "001", "111"}, {"111", "100", "111", "101", "111"},
      {"111", "001", "001", "001", "001"}, {"111", "101", "111", "101", "111"},
      {"111", "101", "111", "001", "111"}};

  static void solution(String[] nums) {
    StringBuilder sb = new StringBuilder();

    for (int i = 0; i < nums[0].length(); i = i + 3) {
      for (int j = 0; j < 10; j++) {
        boolean check = true;
        for (int k = 0; k < 5; k++) {
          if (!nums[k].substring(i, i + 3).equals(num[j][k])) {
            check = false;
            break;
          }
        }
        if (check) {
          sb.append(j);
          break;
        }
      }
    }
    System.out.println(sb);


  }
}
